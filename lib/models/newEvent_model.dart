// Demo model for how the new event should be like.

class NewEventModel {
  final String contactEmail;
  final String date;
  final String eventCity;
  final String eventState;
  final String eventName;
  final String eventStatus;
  final String eventType;
  final String expectedAudience;
  final String fullUserName;
  final String phoneNumber;
  final String time;

  NewEventModel(
      this.contactEmail,
      this.date,
      this.eventCity,
      this.eventState,
      this.eventName,
      this.eventStatus,
      this.eventType,
      this.expectedAudience,
      this.fullUserName,
      this.phoneNumber,
      this.time);
}
